export function getApplicationBySlug(slug) {
  return applicationsData.find((application) => application.slug === slug && application.published !== false);
}

export function getListedApplications() {
  return applicationsData.filter((application) => application.published !== false && !application.isDemo);
}

const PRODUCT_RELATION_ALIASES = {
  ac8: ["omnicure-ac-series", "omnicure-ac8300p-hd-395"],
  "ac8-hd": ["omnicure-ac-series", "omnicure-ac8300p-hd-395"],
  ac9225: ["omnicure-ac-series"],
};

export function getApplicationsForProduct(productSlug, limit) {
  const candidates = [productSlug, ...(PRODUCT_RELATION_ALIASES[productSlug] || [])];
  const matches = applicationsData.filter((application) =>
    application.published !== false && application.relatedProductSlugs.some((slug) => candidates.includes(slug))
  );
  return typeof limit === "number" ? matches.slice(0, limit) : matches;
}

export function getRelatedApplications(application, limit = 3) {
  const industries = Array.isArray(application.industry) ? application.industry : [application.industry];
  return applicationsData
    .filter((candidate) => candidate.published !== false && candidate.slug !== application.slug && (
      (Array.isArray(candidate.industry) ? candidate.industry : [candidate.industry]).some((item) => industries.includes(item)) ||
      candidate.relatedProductSlugs.some((slug) => application.relatedProductSlugs.includes(slug))
    ))
    .slice(0, limit);
}
